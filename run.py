import turtle  # importing turtle module


# creating screen
screen = turtle.Screen()  
screen.bgcolor("#ff0091")
screen.setup(width=1357, height=760)

# creating turtle object
t = turtle.Turtle()
t.speed(0)
t.hideturtle()
t.penup()
# Begin the drawing process
t.backward(500)
t.left(90)
t.forward(200)
t.right(90)
# Draw the blue rectangle
t.pensize(7)
t.pendown()
t.pencolor("white")
t.fillcolor("#ff0091")
t.begin_fill()
t.forward(1000)
t.right(90)
t.forward(500)
t.right(90)
t.forward(1000)
t.right(90)
t.forward(500)
t.end_fill()
# Draw the first semi-circle
t.penup()
t.backward(450)
t.right(90)
t.pendown()
t.circle(200, 180)
t.left(90)
t.forward(150)
t.left(90)
# Draw the first purple shape
t.fillcolor("#ae06f9")
t.begin_fill()
t.forward(150)
t.circle(-40)
t.right(90)
t.forward(80)
t.right(90)
t.forward(150)
t.end_fill()
# Draw the small circle
t.penup()
t.right(90)
t.forward(70)
t.right(90)
t.forward(30)
t.pendown()
t.right(90)
t.forward(30)
t.circle(10)
t.forward(30)
# Move to the next section
t.penup()
t.right(90)
t.forward(30)
t.right(90)
t.forward(271)
t.right(90)
t.forward(500)
t.right(90)
t.pendown()
t.forward(500)
# Draw the second semi-circle
t.penup()
t.left(90)
t.forward(500)
t.left(90)
t.forward(50)
t.left(90)
t.pendown()
t.circle(-200, 180)
t.right(90)
t.forward(150)
t.right(90)
# Draw the second purple shape
t.fillcolor("#ae06f9")
t.begin_fill()
t.forward(150)
t.circle(40)
t.left(90)
t.forward(80)
t.left(90)
t.forward(150)
t.end_fill()
# Draw the small second circle
t.penup()
t.left(90)
t.forward(70)
t.left(90)
t.forward(30)
t.pendown()
t.left(90)
t.forward(30)
t.circle(-10)
t.forward(30)
# Move to the last section
t.penup()
t.left(90)
t.forward(30)
t.left(90)
t.forward(270)
t.left(90)
t.forward(500)
t.left(90)
t.forward(350)
t.pendown()
t.left(90)
t.begin_fill()
t.circle(100, 180)
t.circle(100, 180)
t.end_fill()
# Final line
t.penup()
t.goto(0, 200)
t.right(90)
t.pensize(10)
t.pendown()
t.forward(500)

turtle.done()
